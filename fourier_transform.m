Fs = 512;       %奈奎斯特抽样频率，Fs>2*214
L = 3;
t = 0:1/Fs:L;       %选取包含信号95%~99%能量的时间段L，周期信号可以任意取，此例L=3
                    %或者时域选取根据频率分辨率求得，L=1/d_f
x = cos(2*pi*214*t)+1.7*cos(2*pi*159*t);

N = length(x);      %计算Fourier变换的长度
x_k = fft(x);       %进行Fourier变换

freq = 0:1/L:Fs;       %频率变换的分辨率和范围，其中频率分辨率d_f与L的关系为，d_f=1/L
plot(freq,1/Fs*abs(x_k))       %绘制图像， 注意fft变换的样值乘以1/Fs才是连续傅里叶变换的样值
